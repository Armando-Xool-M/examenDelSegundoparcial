package com.example.demo.modelo;

public class planetasDTO {
    private double UA;
    private int id;
    private double milesk;
    private String nombre;
	public double getUA() {
		return UA;
	}
	public void setUA(double uA) {
		UA = uA;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getMilesk() {
		return milesk;
	}
	public void setMilesk(double milesk) {
		this.milesk = milesk;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
    
    
}
