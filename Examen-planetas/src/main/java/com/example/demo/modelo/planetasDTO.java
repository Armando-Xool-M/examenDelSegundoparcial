package com.example.demo.modelo;

public class planetasDTO {
	
    private float UA;
    private int id;
    private double milesk;
    private String nombre;
    
	public float getUA() {
		return UA;
	}
	public void setUA(float UA) {
		this.UA = UA;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getMilesk() {
		return milesk;
	}
	public void setMilesk(double milesk) {
		this.milesk = milesk;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
    
}
